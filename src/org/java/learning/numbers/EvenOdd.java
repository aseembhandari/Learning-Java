package org.java.learning.numbers;

/**
 * This class checks whether number is even or odd.
 * */
public class EvenOdd {
	public static void main(String[] args) {
		int number = 10;
		if (number % 2 == 0) {
			System.out.print("number is even");
		} else {
			System.out.println("number is odd");
		}
	}
}
