package org.java.learning.numbers;
/**
 * This program demonstrates conditional statement in Java
 * */
public class IfElse {

	public static void main(String args[]) {
		int test = 45;

		if (test < 9) {
			System.out.println("Number is less than 9");
		} else {
			System.out.println("Number is greater than 9");
		}
	}

}
